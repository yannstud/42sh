#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	int		szc;

	i = 0;
	szc = 0;
	while (s[szc] != '\0')
		szc++;
	if (!(dest = (char*)malloc(sizeof(char) * szc + 1)))
		return (0);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
