#include "libft.h"

void	ft_strclr(char *s)
{
	int		i;
	int		n;

	if (s)
	{
		i = 0;
		n = ft_strlen(s);
		while (i < n)
		{
			s[i] = '\0';
			i++;
		}
	}
}
